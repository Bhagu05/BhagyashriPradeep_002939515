package Model;

/**
 *
 * @author bhagy
 */
public class ContactInfo {
   String email;
   long phoneNumber;
   
   public ContactInfo()
   {
       this.email="";
       this.phoneNumber=0;
      
   }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public long getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(long phoneNumber) {
        this.phoneNumber = phoneNumber;
    }
}

    
